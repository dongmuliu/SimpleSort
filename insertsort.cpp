/*
（有序区，无序区）。把无序区的第一个元素插入到有序区的合适的位置。对数组：比较得少，换得多。
插入排序思路：
1. 从第一个元素开始，该元素可以认为已经被排序
2. 取出下一个元素，在已经排序的元素序列中从后向前扫描
3. 如果该元素（已排序）大于新元素，将该元素移到下一位置
4. 重复步骤3，直到找到已排序的元素小于或者等于新元素的位置
5. 将新元素插入到该位置后
6. 重复步骤2~5
*/
#include<string>
#include<vector>
#include<iostream>
#include<stdio.h>
using namespace std;
//插入排序
//输入：需排序向量
void InsertSort(vector<int>&insertvector)
{
	int temp;
	for (int i = 1; i < insertvector.size(); i++)
	{
		temp = insertvector[i];
		for (int j = i-1; j >=0; j--)
		{
			//满足条件，移动元素位置
			if (insertvector[j] >temp)
			{
				insertvector[j+1] = insertvector[j];
				insertvector[j] = temp;
				

			}
			else
			{
				break;
			}
			
		}

	}
}
int main()
{
	int b;
	vector<int>inputvector;
	//回车停止输入循环
	while (cin >> b)
	{
		inputvector.push_back(b);
		char c = cin.get();
		if (c == '\n')
			break;
	}
	InsertSort(inputvector);
	//输出排序后的向量
	for (int i = 0; i < inputvector.size(); i++)
	{
		printf("%d ", inputvector[i]);
	}
	//避免控制台闪退
	system("pause");
}